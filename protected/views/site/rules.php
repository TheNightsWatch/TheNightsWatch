<p>We, the Night's Watch, self-declared protectors of the realm - have
	seen chaos ensue the land, with public servers increasingly growing
	towards brave survivors killing each other on sight. We seek an end to
	this growing ruthless hostility, and have thus compiled a list of
	aggressive actions. Taking any of the listed actions below will warrant
	the immediate and merciless destruction of your life.</p>

<p>Abiding by these rules does not mean that you must enforce them. If a
	person you deeply trust does any of the below, adhering to these rules
	in no way means that you need attack them. These rules are simply
	guidelines in order to best establish when it is okay to stop being
	friendly and attack in order to save your own life.</p>

<p>As stated above, taking any of the below actions will be counted as
	aggression:</p>

<ul>
	<li>Running directly towards a player</li>
	<li>Offensively attacking a player</li>
	<li>Approaching a player within a 15 block radius</li>
	<li>Approaching a player with your sword out.

		<ul>
			<li>As the common signal of peace is to put your swords in defensive
				motion, it is advised that after you are sure a player has seen you
				do so, to swap out to a non-harming item such as water or (even
				better) shears. Additionally, in order to protect your own life, you
				should not approach the player while his or her sword is still
				drawn.</li>
		</ul>
	</li>
	<li>Following a player (especially off the road)</li>
	<li>Drawing back a bow for any length of time towards a player</li>
</ul>

<p>Taking any of these actions will be viewed as aggression and your
	life may be forfeit.</p>

